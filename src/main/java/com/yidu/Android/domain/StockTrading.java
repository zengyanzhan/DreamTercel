package com.yidu.Android.domain;

import java.sql.Date;

/**
 * 券商成交量统计表
 * @author Lee
 * @date 2017年12月5日
 * @time 下午3:09:00
 */
public class StockTrading {
private String strDealDate;
private Date dealDate;
private String tradeSeatCode;
private Integer count;
public StockTrading(){}
public StockTrading(String tradeSeatCode, Integer count) {
	super();
	this.tradeSeatCode = tradeSeatCode;
	this.count = count;
}
public Date getDealDate() {
	return dealDate;
}
public void setDealDate(Date dealDate) {
	this.dealDate = dealDate;
}

public String getStrDealDate() {
	return strDealDate;
}
public void setStrDealDate(String strDealDate) {
	this.strDealDate = strDealDate;
}
public String getTradeSeatCode() {
	return tradeSeatCode;
}
public void setTradeSeatCode(String tradeSeatCode) {
	this.tradeSeatCode = tradeSeatCode;
}
public Integer getCount() {
	return count;
}
public void setCount(Integer count) {
	this.count = count;
}

}

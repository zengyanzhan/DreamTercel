package com.yidu.system.domain;

import java.io.Serializable;
import java.sql.Date;


/**
 * 节假日
 * @author XiaoYuJie
 * @date 2017年11月24日
 * @time 上午9:37:00
 */
public class HoildayXiao implements Serializable{
	private Integer hCode;//节假日Id
	private Date  hDate;//节假日日期
	private Integer hYear;//节假日年份
	private Integer hMonth;//节假日月份
	private Integer hDay;//节假日日号
	private String hDesc;//节假日备注
	private String strDate;
	
	public HoildayXiao(){}
	
	
	
	public HoildayXiao(Integer hCode, Date hDate, Integer hYear, Integer hMonth, Integer hDay, String hDesc,String strDate) {
		super();
		this.hCode = hCode;
		this.hDate = hDate;
		this.hYear = hYear;
		this.hMonth = hMonth;
		this.hDay = hDay;
		this.hDesc = hDesc;
		this.strDate=strDate;
	}



	public Integer gethCode() {
		return hCode;
	}
	public void sethCode(Integer hCode) {
		this.hCode = hCode;
	}
	public Date gethDate() {
		return hDate;
	}
	public void sethDate(Date hDate) {
		this.hDate = hDate;
	}
	public Integer gethYear() {
		return hYear;
	}
	public void sethYear(Integer hYear) {
		this.hYear = hYear;
	}
	public Integer gethMonth() {
		return hMonth;
	}
	public void sethMonth(Integer hMonth) {
		this.hMonth = hMonth;
	}
	public Integer gethDay() {
		return hDay;
	}
	public void sethDay(Integer hDay) {
		this.hDay = hDay;
	}
	public String gethDesc() {
		return hDesc;
	}
	public void sethDesc(String hDesc) {
		this.hDesc = hDesc;
	}

	

	public String getStrDate() {
		return strDate;
	}



	public void setStrDate(String strDate) {
		this.strDate = strDate;
	}

	@Override
	public String toString() {
		return "HoildayXiao [hCode=" + hCode + ", hDate=" + hDate + ", hYear=" + hYear + ", hMonth=" + hMonth
				+ ", hDay=" + hDay + ", hDesc=" + hDesc + ", strDate=" + strDate + "]";
	}



	
	
}

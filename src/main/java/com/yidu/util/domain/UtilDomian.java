package com.yidu.util.domain;

import java.sql.Date;
 
public class UtilDomian {

	private String tableName;
	private String cardField;
	private String dateField;
	private Date strDate;
	private String treeFatherCode;
	private Date statisticDate;
	
	public String getTreeFatherCode() {
		return treeFatherCode;
	}
	public void setTreeFatherCode(String treeFatherCode) {
		this.treeFatherCode = treeFatherCode;
	}
	public Date getStatisticDate() {
		return statisticDate;
	}
	public void setStatisticDate(Date statisticDate) {
		this.statisticDate = statisticDate;
	}
	public String getTableName() {
		return tableName;
	}
	public void setTableName(String tableName) {
		this.tableName = tableName;
	}
	public String getCardField() {
		return cardField;
	}
	public void setCardField(String cardField) {
		this.cardField = cardField;
	}
	public String getDateField() {
		return dateField;
	}
	public void setDateField(String dateField) {
		this.dateField = dateField;
	}
	public Date getStrDate() {
		return strDate;
	}
	public void setStrDate(Date strDate) {
		this.strDate = strDate;
	}
	public UtilDomian(){}
	public UtilDomian(String tableName, String cardField, String dateField, Date strDate) {
		super();
		this.tableName = tableName;
		this.cardField = cardField;
		this.dateField = dateField;
		this.strDate = strDate;
	}
	public UtilDomian(String tableName, String cardField, String dateField, Date strDate, String treeFatherCode,
			Date statisticDate) {
		super();
		this.tableName = tableName;
		this.cardField = cardField;
		this.dateField = dateField;
		this.strDate = strDate;
		this.treeFatherCode = treeFatherCode;
		this.statisticDate = statisticDate;
	}
	public UtilDomian(String treeFatherCode, Date statisticDate) {
		super();
		this.treeFatherCode = treeFatherCode;
		this.statisticDate = statisticDate;
	}
	
	
}

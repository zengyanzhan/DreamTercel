package com.yidu.cashManagement.domain;

import java.sql.Date;

/**
 * 资金调拨实体类
 * @author 肖光宇
 * @date 2017年11月13日
 * @time 上午10:46:43
 *
 */
public class MoneyAllot {
	private String code;//资金调拨编号
	private String fundCode;//基金编号
	private Double money;//调拨资金数额
	private String accountCode;//现金账户
	private Integer direction;//资金调拨方向
	private Date date;//调拨日期
	private Date businessDate;//业务日期
	private String businessCode;//业务编号
	private Integer type;//调拨类型
	private String desc;//备注
	private Integer page;//页
	private Integer rows;//行
	private String sortOrder;//排序的列
	private String allotType;
	private String typeStr;
	private String strDate;
	private String strBusinessDate;
	public MoneyAllot(){}
	public MoneyAllot(String code, String fundCode, Double money, String accountCode, Integer direction, Date date,
			Date businessDate, String businessCode, Integer type, String desc) {
		super();
		this.code = code;
		this.fundCode = fundCode;
		this.money = money;
		this.accountCode = accountCode;
		this.direction = direction;
		this.date = date;
		this.businessDate = businessDate;
		this.businessCode = businessCode;
		this.type = type;
		this.desc = desc;
	}
	public String getStrBusinessDate() {
		return strBusinessDate;
	}

	public void setStrBusinessDate(String strBusinessDate) {
		this.strBusinessDate = strBusinessDate;
	}

	public String getStrDate() {
		return strDate;
	}

	public void setStrDate(String strDate) {
		this.strDate = strDate;
	}

	public String getTypeStr() {
		return typeStr;
	}

	public void setTypeStr(String typeStr) {
		this.typeStr = typeStr;
	}

	public String getAllotType() {
		return allotType;
	}

	public void setAllotType(String allotType) {
		this.allotType = allotType;
	}

	public String getSortOrder() {
		return sortOrder;
	}

	public void setSortOrder(String sortOrder) {
		this.sortOrder = sortOrder;
	}

	public Integer getPage() {
		return page;
	}

	public void setPage(Integer page) {
		this.page = page;
	}


	public Integer getRows() {
		return rows;
	}

	public void setRows(Integer rows) {
		this.rows = rows;
	}

	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getFundCode() {
		return fundCode;
	}
	public void setFundCode(String fundCode) {
		this.fundCode = fundCode;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public String getAccountCode() {
		return accountCode;
	}
	public void setAccountCode(String accountCode) {
		this.accountCode = accountCode;
	}
	public Integer getDirection() {
		return direction;
	}
	public void setDirection(Integer direction) {
		this.direction = direction;
	}
	public Date getDate() {
		return date;
	}
	public void setDate(Date date) {
		this.date = date;
	}
	public Date getBusinessDate() {
		return businessDate;
	}
	public void setBusinessDate(Date businessDate) {
		this.businessDate = businessDate;
	}
	public String getBusinessCode() {
		return businessCode;
	}
	public void setBusinessCode(String businessCode) {
		this.businessCode = businessCode;
	}
	public Integer getType() {
		return type;
	}
	public void setType(Integer type) {
		this.type = type;
	}
	public String getDesc() {
		return desc;
	}
	public void setDesc(String desc) {
		this.desc = desc;
	}


	@Override
	public String toString() {
		return "MoneyAllot [code=" + code + ", fundCode=" + fundCode + ", money=" + money + ", accountCode="
				+ accountCode + ", direction=" + direction + ", date=" + date + ", businessDate=" + businessDate
				+ ", businessCode=" + businessCode + ", type=" + type + ", desc=" + desc + ", page=" + page + ", rows="
				+ rows + ", sortOrder=" + sortOrder + ", allotType=" + allotType + ", typeStr=" + typeStr + ", strDate="
				+ strDate + ", strBusinessDate=" + strBusinessDate + "]";
	}








}

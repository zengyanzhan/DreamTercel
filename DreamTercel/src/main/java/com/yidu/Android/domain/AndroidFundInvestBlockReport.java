package com.yidu.Android.domain;

import java.sql.Date;

/**
 * 基金投资板块表
 * @author 杨丽
 * @date 2017年12月7日	
 * @time 上午10:53:51
 *
 *
 */
public class AndroidFundInvestBlockReport {
	private String stockBlockName;	//证券板块名称
	private Integer assecurityQuantity;	//证券数量
	private Double marketValue;	// 市值
	private String projectCode;	//项目代码
	private String fundCode;	//基金代码
	private Date statisticDate;	//统计日期
	
	private Double marketValuePercentage;	//市值占净值百分比
	private String statisticDateWhere;	//统计日期
	

	
	private String tableName;			//表名
	private Integer page;				//页数
	private Integer rows;				//行数
	private Integer rowsTotal;			//总行数
	private String orderColumn; 		//排序的列
	private String orderStyle;			//排序方式

	/**
	 * 构造方法
	 */
	public AndroidFundInvestBlockReport(){}

	public AndroidFundInvestBlockReport(String stockBlockName, Integer assecurityQuantity, Double marketValue, String projectCode,
			String fundCode, Date statisticDate,String statisticDateWhere) {
		super();
		this.stockBlockName = stockBlockName;
		this.assecurityQuantity = assecurityQuantity;
		this.marketValue = marketValue;
		this.projectCode = projectCode;
		this.fundCode = fundCode;
		this.statisticDate = statisticDate;
		this.statisticDateWhere=statisticDateWhere;
	}

	/**
	 * get/set方法
	 * 
	 */
	public String getStockBlockName() {
		return stockBlockName;
	}

	public void setStockBlockName(String stockBlockName) {
		this.stockBlockName = stockBlockName;
	}

	public Integer getAssecurityQuantity() {
		return assecurityQuantity;
	}

	public void setAssecurityQuantity(Integer assecurityQuantity) {
		this.assecurityQuantity = assecurityQuantity;
	}

	public Double getMarketValue() {
		return marketValue;
	}

	public void setMarketValue(Double marketValue) {
		this.marketValue = marketValue;
	}

	public String getProjectCode() {
		return projectCode;
	}

	public void setProjectCode(String projectCode) {
		this.projectCode = projectCode;
	}

	public String getFundCode() {
		return fundCode;
	}

	public void setFundCode(String fundCode) {
		this.fundCode = fundCode;
	}

	public Date getStatisticDate() {
		return statisticDate;
	}

	public void setStatisticDate(Date statisticDate) {
		this.statisticDate = statisticDate;
	}

	public String getStatisticDateWhere() {
		return statisticDateWhere;
	}

	public void setStatisticDateWhere(String statisticDateWhere) {
		this.statisticDateWhere = statisticDateWhere;
	}

	public String getTableName() {
		return tableName;
	}

	public void setTableName(String tableName) {
		this.tableName = tableName;
	}

	public Integer getPage() {
		return page;
	}

	public void setPage(Integer page) {
		this.page = page;
	}

	public Integer getRows() {
		return rows;
	}

	public void setRows(Integer rows) {
		this.rows = rows;
	}

	public Integer getRowsTotal() {
		return rowsTotal;
	}

	public void setRowsTotal(Integer rowsTotal) {
		this.rowsTotal = rowsTotal;
	}

	public String getOrderColumn() {
		return orderColumn;
	}

	public void setOrderColumn(String orderColumn) {
		this.orderColumn = orderColumn;
	}

	public String getOrderStyle() {
		return orderStyle;
	}

	public void setOrderStyle(String orderStyle) {
		this.orderStyle = orderStyle;
	}
	
	
	

	public Double getMarketValuePercentage() {
		return marketValuePercentage;
	}

	public void setMarketValuePercentage(Double marketValuePercentage) {
		this.marketValuePercentage = marketValuePercentage;
	}

	@Override
	public String toString() {
		return "FundlnvestBlock [stockBlockName=" + stockBlockName + ", assecurityQuantity=" + assecurityQuantity
				+ ", marketValue=" + marketValue + ", projectCode=" + projectCode + ", fundCode=" + fundCode
				+ ", statisticDate=" + statisticDate + ", statisticDateWhere=" + statisticDateWhere + ", tableName="
				+ tableName + ", page=" + page + ", rows=" + rows + ", rowsTotal=" + rowsTotal + ", orderColumn="
				+ orderColumn + ", orderStyle=" + orderStyle + "]";
	}
	
	
}
